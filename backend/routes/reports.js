router.get('/drilldown', auth, async (req, res) => {
  const { month, type } = req.query;
  const page = parseInt(req.query.page, 10) || 1;
  const pageSize = parseInt(req.query.pageSize, 10) || 50;
  try {
    if (type === 'donor') {
      // filter donors by lastDonationDate month if provided
      const match = {};
      if (month) {
        // simple month name match against ISO month
        const monthNames = { Jan:0, Feb:1, Mar:2, Apr:3, May:4, Jun:5, Jul:6, Aug:7, Sep:8, Oct:9, Nov:10, Dec:11 };
        const m = monthNames[String(month).slice(0,3)];
        if (typeof m === 'number') {
          const start = new Date(new Date().getFullYear(), m, 1);
          const end = new Date(new Date().getFullYear(), m+1, 1);
          match.lastDonationDate = { $gte: start, $lt: end };
        }
      }
      const total = await Donor.countDocuments(match);
      const rows = await Donor.find(match).skip((page-1)*pageSize).limit(pageSize).lean();
      return res.json({ rows: rows.map(r=>({ donor: r.name, amount: r.donations, date: r.lastDonationDate })), total });
    }

    // fallback: return campaign or emergency items
    if (type === 'campaign') {
      const total = await Campaign.countDocuments();
      const rows = await Campaign.find().skip((page-1)*pageSize).limit(pageSize).lean();
      return res.json({ rows, total });
    }

    if (type === 'hospital') {
      const total = await Emergency.countDocuments();
      const rows = await Emergency.find().skip((page-1)*pageSize).limit(pageSize).lean();
      return res.json({ rows, total });
    }

    return res.json({ rows: sampleMonths, total: sampleMonths.length });
  } catch (err) {
    console.error('reports/drilldown error', err && err.message);
    return res.status(500).json({ message: 'Failed to fetch drilldown' });
  }
});

